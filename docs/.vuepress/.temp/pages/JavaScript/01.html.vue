<h2 id="_1-1-js的前世今生" tabindex="-1"><a class="header-anchor" href="#_1-1-js的前世今生"><span>1.1 <code v-pre>js</code>的前世今生</span></a></h2>
<p>​	<code v-pre>JavaScript</code>一种直译式脚本语言，是一种<strong>动态类型</strong>、<strong>弱类型</strong>、基于原型的语言，最早是在<code v-pre>HTML</code>网页上使用，用来给HTML网页增加动态功能。</p>
<p>​	<strong>动态</strong>：在运行时确定数据类型。变量使用之前不需要类型声明，通常变量的类型是被赋值的那个值的类型。</p>
<p>​	<strong>弱类</strong>：计算时可以不同类型之间对使用者透明地隐式转换，即使类型不正确，也能通过隐式转换来得到正确的类型。</p>
<p>​	<strong>原型</strong>：新对象继承对象（作为模版），将自身的属性共享给新对象，模版对象称为原型。这样新对象实例化后不但可以享有自己创建时和运行时定义的属性，而且可以享有原型对象的属性。</p>
<p>​	<strong>脚本语言</strong>：不需要编译器编译。</p>
<p>​</p>
<p>​	在1995年时，由 <code v-pre>Netscape</code> 公司的布兰登·艾奇（<code v-pre>Brendan Eich</code>，1961年～），<code v-pre>JavaScript</code>的发明人，在网景导航者浏览器（<code v-pre>Navigator</code>）上首次设计实现而成。</p>
<p>​	由于网景公司希望能在静态 <code v-pre>HTML</code> 页面上添加一些动态效果，于是叫<code v-pre>Brendan Eich</code>这哥们在两周之内设计出了<code v-pre>JavaScript</code>语言。你没看错，这哥们只用了 10 天时间。</p>
<p>​	为什么起名叫 <code v-pre>JavaScript</code>？</p>
<p>​	原因是当时<code v-pre>Java</code>语言非常红火，所以网景公司希望借 <code v-pre>Java</code> 的名气来推广，但事实上 <code v-pre>JavaScript</code> 除了语法上有点像 <code v-pre>Java</code>，其他部分基本上没啥关系。</p>
<p>​	<code v-pre>Netscape</code> 在最初将其脚本语言命名为 <code v-pre>LiveScript</code>，后来 <code v-pre>Netscape</code> 在与 <code v-pre>Sun</code> 合作之后将其改名为 <code v-pre>JavaScript</code>。</p>
<p>​	<code v-pre>JavaScript</code> 的三个主要组成部分是：</p>
<ul>
<li><strong><code v-pre>ECMAScript</code>(核心)</strong></li>
<li><strong><code v-pre>DOM</code>（文档对象模型</strong></li>
<li><strong><code v-pre>BOM</code>（浏览器对象模型）</strong>。</li>
</ul>
